							<h2 class="gigantic text-center">Wormholes</h2>
							<p class="big text-center"><br /><i class="fa fa-diamond"></i></p>
							<p class="big italic text-center">Einstein-Rosen Bridge is a hypothetical topological feature that would fundamentally be a shortcut connecting two separate points in spacetime. A wormhole, in theory, might be able to connect extremely far distances such as a billion light years or more, short distances such as a few feet, different universes, and different points in time. </p>
							<p class="text-center"><img src="https://upload.wikimedia.org/wikipedia/commons/d/d2/Lorentzian_Wormhole.svg" alt="wormhole"></p>
							<br />
							<p>Lorentzian traversable wormholes would allow travel in both directions from one part of the universe to another part of that same universe very quickly or would allow travel from one universe to another. The possibility of traversable wormholes in general relativity was first demonstrated by Kip Thorne and his graduate student Mike Morris in a 1988 paper. For this reason, the type of traversable wormhole they proposed, held open by a spherical shell of exotic matter, is referred to as a Morris–Thorne wormhole. Later, other types of traversable wormholes were discovered as allowable solutions to the equations of general relativity, including a variety analyzed in a 1989 paper by Matt Visser, in which a path through the wormhole can be made where the traversing path does not pass through a region of exotic matter.</p>
							<p>However, in the pure Gauss–Bonnet gravity (a modification to general relativity involving extra spatial dimensions which is sometimes studied in the context of brane cosmology) exotic matter is not needed in order for wormholes to exist—they can exist even with no matter.[17] A type held open by negative mass cosmic strings was put forth by Visser in collaboration with Cramer et al.,[11] in which it was proposed that such wormholes could have been naturally created in the early universe.</p>
							<p>Wormholes connect two points in spacetime, which means that they would in principle allow travel in time, as well as in space. In 1988, Morris, Thorne and Yurtsever worked out explicitly how to convert a wormhole traversing space into one traversing time.[3] However, according to general relativity, it would not be possible to use a wormhole to travel back to a time earlier than when the wormhole was first converted into a time machine by accelerating one of its two mouths.</p>
