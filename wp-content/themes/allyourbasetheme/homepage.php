										<p>In the standard model of the evolution of the universe, galactic filaments form along and follow web-like strings of dark matter.[4] It is thought that this dark matter dictates the structure of the Universe on the grandest of scales. Dark matter gravitationally attracts baryonic matter, and it is this "normal" matter that astronomers see forming long, thin walls of super-galactic clusters.</p>
										<p>The CfA2 Great Wall (or Coma Wall) was the first super-large large-scale structure or pseudo-structure in the universe to be discovered. The CfA Homunculus lies at the heart of the Great Wall, and the Coma Supercluster forms most of the homunculus structure. The Coma Cluster lies at the core.</p>
