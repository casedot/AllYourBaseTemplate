										<p>This assumes the quasar is radiating energy in all directions, but the active galactic nucleus is believed to be radiating preferentially in the direction of its jet. In a universe containing hundreds of billions of galaxies, most of which had active nuclei billions of years ago but only seen today, it is statistically certain that thousands of energy jets should be pointed toward us, some more directly than others. In many cases it is likely that the brighter the quasar, the more directly its jet is aimed at us.</p>	
