									<div class="medium-5 columns">
										<p>More than 200,000 quasars are known, most from the Sloan Digital Sky Survey. All observed quasar spectra have redshifts between 0.056 and 7.085. Applying Hubble's law to these redshifts, it can be shown that they are between 600 million[13] and 28.85 billion light-years away (in terms of comoving distance). Because of the great distances to the farthest quasars and the finite velocity of light, we see them and their surrounding space as they existed in the very early universe.</p>
										<p>The power of quasars originates from supermassive black holes that are believed to exist at the core of all galaxies. A survey of the 40 nearest galaxies with the Hubble Space Telescope in the 90's revealed Doppler shifts of the stars near the core of those galaxies were rotating about tremendous masses with very steep gravity gradients, suggesting black holes.</p>
										<p>Although quasars appear faint when viewed from Earth, they are visible from extreme distances because quasars are the most luminous objects in the known universe.</p>
