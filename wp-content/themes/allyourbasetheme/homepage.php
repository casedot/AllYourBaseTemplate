										<p>Supernovae fuse and eject the bulk of the primary elements of nucleosynthesis.[4] They are potentially strong galactic sources of gravitational waves.[5] A great proportion of primary cosmic rays comes from supernovae.[6] Their many significant consequences make supernovae the most important stellar events in astronomy.</p>
